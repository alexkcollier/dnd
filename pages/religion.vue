        <h1 class="title">Religion</h1>

        <h2 class="title">The Grey Faith</h2>

        <p>The main religion in civilized areas is the Grey Faith. It holds the premise that the world is composed of Light and
        Dark, and one cannot exist without the other. The Light is usually associated with life, joy, victory, and art, while
        the Dark is associated with death, loss, secrecy, and sleep. There are adherents who are drawn toward either the
        Light or the Dark, but as elements of Dark and Light are heavily entwined in all aspects of life and the world, they
        are most commonly venerated together. It is this blending from which the Faith takes its name.</p>

        <p>Dark and Light are not viewed as deities, as that entire concept is from a less enlightened time. Instead, it is more
        accurate to view them as energies, or forces. At first glance, it may seem that Light represents goodness, and Dark
        represents evil, this is not entirely true. The pursuit of joy has led many to fall into depraved lives, and the
        recovery from loss has formed the most celebrated heroes.</p>

        <p>Clerics of the Grey Faith may belong to any domain, depending on how that manifests. Knowledge, Light, and Trickery
        are most common for Grey, Light, and Dark clerics, respectively. Many of the Grey Faith's clergy are not clerics,
        but monks or paladins instead. Clerics devote themselves to the care of all, monks to the pursuit of self-perfection
        and enlightenment, and paladins to specific causes of the faith.</p>

        <h2 class="title">Folk Religion</h2>

        <p>As you move further from the major cities, the concept of deities grows more prevalent. In small towns and villages,
        stories are told of when powerful beings formed the world and warred against each other. Not wishing to mar their
        creation further, they have since retreated from the physical world, though their influence can still be felt. Many
        pantheons of deities have been suggested. The most popular is shown below, however others still hold sway.</p>

        <p>The concept of deities becomes less common again as you move into older, wilder places. Here, religions become animistic
        instead, and for good reason. In these old, untamed places, there are rumours that powerful spirits manifest and
        walk through them.</p>

