        <p><b>Asmodeus</b> is the evil god of tyranny, domination and sin. He is the King of Hell, ruling the Nine Hells of Baator,
        and father of Glasya, Duke of the Sixth Hell, Malbolge. He was once an archangel and exarch. Asmodeus betrayed his
        god, killing him and had the devils erase all knowledge of the god's name for fear that the mere mention of it would
        revive him.</p>

        <p><b>Avandra</b> delights in freedom, trade, travel, adventure, and the frontier. Her temples are few in civilized lands,
        but her wayside shrines appear throughout the world. Halflings, merchants, and all types of adventurers are drawn
        to her worship, and many people raise a glass in her honor, viewing her as the god of luck. Her commandments are
        few:
        </p>
        <ul>
          <li>Luck favors the bold. Take your fate into your own hands, and Avandra smiles upon you.</li>
          <li>Strike back against those who would rob you of your freedom and urge others to fight for their own liberty.</li>
          <li>Change is inevitable, but it takes the work of the faithful to ensure that change is for the better.</li>
        </ul>

        <p><b>Bahamut</b>, also called the Platinum Dragon, is the good-aligned god of justice, protection and nobilitiy. He is
        the patron of the dragonborn and the good-aligned metallic dragons. He was created when the dragon god Io was split
        in half. The other half became Tiamat, patron goddess of the evil-aligned chromatic dragons. He is stern, but kind
        and helpful to the downtrodden. His clerics and followers are taught to oppose evil and help those in need.</p>

        <p><b>Bane</b> is the evil-aligned god of war and conquest. He battles endlessly with Gruumsh for supremacy.</p>

        <p><b>Corellon</b> is the deity of spring, the arts, magic, and beauty. He is the patron of the arcane arts, and is most
        frequently worshiped by elves and eladrin. He seeded the world with arcane magic and planted the most ancient forests.
        Artists and musicians worship him, as do those who view their spellcasting as an art. He despises Lolth and her priestesses
        for leading the drow astray. He urges his followers thus:</p>
        <ul>
          <li>Cultivate beauty in all that you do, whether you’re casting a spell, composing a saga, strumming a lute, or practicing
          the arts of war.</li>
          <li>Seek out lost magic items, forgotten rituals, and ancient works of art. Corellon might have inspired them in the
          world’s first days.</li>
          <li>Thwart the followers of Lolth at every opportunity.</li>
        </ul>

        <p><b>Erathis</b> is the unaligned god of civilization, inventions and law. Rulers, judges, pioneers and devoted citizens
        worship her, and her temples are present in most major cities of the world. She lives with Pelor in Hestavar, where
        they are known as "the King and Queen of Light" and rule the city. She is allied with Moradin and Ioun. She is perhaps
        the only god on cordial terms with Asmodeus, King of the Nine Hells, as tyranny and dystopia are forms of civilization.
        Her antithesis is Melora. Erathis commands her devoted followers with three of her many laws:</p>
        <ul>
          <li>Work with others to achieve your goals. Community and order are always stronger than individuals.</li>
          <li>Tame the wilderness to make it fit for habitation, and defend the light of civilization against darkness.</li>
          <li>Seek out new ideas, inventions, lands and wilderness. Build machines, cities and empires.</li>
        </ul>

        <p><b>Gruumsh</b> is the chaotic evil god of destruction, and the lord of marauding hordes, though some are known to worship
        him as a war god. Many orc priests wear eye patches to show their devotion to Gruumsh. The followers of Gruumsh are
        told to conquer and destroy, crush the weak, breed and let their numbers flourish, and let nothing stand in their
        way.
        </p>

        <p><b>Ioun</b> is the god of knowledge, skill, and prophecy. Sages, seers, and tacticians revere her, as do all who live
        by their knowledge and mental power. Corellon is the patron of arcane magic, but Ioun is the patron of its study.
        Libraries and wizard academies are built in her name. She is often considered to be Vecna's antithesis, because she
        wishes to share with the world all the knowledge that Vecna would rather keep secret. Her commands are also teachings:</p>
        <ul>
          <li>Seek the perfection of your mind by bringing reason, perception, and emotion into balance with one another. </li>
          <li>Accumulate, preserve, and distribute knowledge in all forms. Pursue education, build libraries, and seek out lost
          and ancient lore. </li>
          <li>Be watchful at all times for the followers of Vecna, who seek to control knowledge and keep secrets. Oppose their
          schemes, unmask their secrets, and blind them with the light of truth and reason.</li>
        </ul>

        <p><b>Kord</b> is the storm god and the lord of battle. He revels in strength, battlefield prowess, and thunder. Fighters
        and athletes revere him. He is a mercurial god, unbridled and wild, who summons storms over land and sea; those who
        hope for better weather appease him with prayers and spirited toasts. He gives few commands:</p>
        <ul>
          <li>Be strong, but do not use your strength for wanton destruction.</li>
          <li>Be brave and scorn cowardice in any form.</li>
          <li>Prove your might in battle to win glory and renown.</li>
        </ul>

        <p><b>Lolth</b>, also called the Spider Queen and the Queen of the Demonweb Pits, is the goddess of shadow, lies, and
        spiders and the patron of the drow. Scheming and treachery are her commands, and her clerics are a constant force
        of disruption in the otherwise stable society of the evil drow. Lolth is a cruel and demanding goddess that expects
        absolute loyalty from her followers. She will often test them and selects drow of great skill and power for her trial.</p>

        <p><b>Melora</b> is often worshiped by elves as well as hunters and rangers. As the goddess of nature, wilderness and
        the sea, she is the antithesis of Erathis, the deity of civilization, and is also known to dislike Asmodeus. She
        has no astral domain and prefers to simply wander the cosmos. Sailors often make offerings to her before they embark
        on their voyages. She commands her followers thusly: </p>
        <ul>
          <li>Protect the wild places of the world from destruction and overuse. Oppose the rampant spread of cities and empires.</li>
          <li>Hunt aberrant monsters and other abominations of nature. </li>
          <li>Do not fear or condemn the savagery of the wild. Live in harmony with the wild. </li>
        </ul>

        <p><b>Moradin</b> is the god of creation and patron of artisans, especially miners and smiths. He carved the mountains
        from primordial earth and is the guardian and protector of the hearth and the family. Dwarves from all walks of life
        follow him. He demands these behaviors of his followers:</p>
        <ul>
          <li>Meet adversity with stoicism and tenacity.</li>
          <li>Demonstrate loyalty to your family, your clan, your leaders, and your people.</li>
          <li>Strive to make a mark on the world, a lasting legacy. To make something that lasts is the highest good, whether you
          are a smith working at a forge or a ruler building a dynasty.</li>
        </ul>

        <p><b>Pelor</b> is the good aligned god of the sun, agriculture and time as well as the seasonal god of summer. He is
        known as a healing god and is the most popular god among humans. His clerics are well respected and honored in most
        good aligned realms. He dwells in the in a palace called Aurosion in Hestavar with Erathis who it is implied he is
        in a relationship with. The two are known as "the King and Queen of Light". While he opposes evil and the evil gods,
        he is the greatest enemy of the Chained God Tharizdun. His antithesis is the Raven Queen, goddess of death, fate
        and doom, and the seasonal goddess of winter.</p>

        <p>The <b>Raven Queen's</b> true name is long forgotten. She is the goddess of death, spinner of fate, and patron of winter.
        She marks the end of each mortal life, and mourners call upon her during funeral rites, in the hope that she will
        guard the departed from the curse of undeath. She expects her followers to abide by these commandments:</p>
        <ul>
          <li>Hold no pity for those who suffer and die, for death is the natural end of life.</li>
          <li>Bring down the proud who try to cast off the chains of fate. As the instrument of the Raven Queen, you must punish
          hubris where you find it.</li>
          <li>Watch for the cults of Orcus and stamp them out whenever they arise. The Demon Prince of the Undead seeks to claim
          the Raven Queen's throne.</li>
        </ul>

        <p><b>Sehanine</b>, goddess of the moon and autumn, is the patron of trickery and illusions. She has close ties to Corellon
        and Melora and is a favorite deity among elves and halflings. Scouts and thieves ask for her blessing on their work.
        Her teachings are simple:</p>
        <ul>
          <li>Follow your goals and seek your own destiny.</li>
          <li>Keep to the shadows, avoiding the blazing light of zealous good and the utter darkness of evil.</li>
          <li>Seek new horizons and new experiences, and let nothing tie you down.</li>
        </ul>

        <p><b>Tharizdun</b> is the evil god of madness and annihilation. He found power in a shard of evil at the edge of the
        universe. When he touched the shard, he was transformed and twisted by the evil power of the shard into something
        unrecognizable, spawning the Infinite Layers of the Abyss and the demons. As a result of creating the Abyss, Tharizdun
        was imprisoned by the other gods and sealed away in another plane. His name is not known by many, even among his
        followers. He is often referred to as the Chained God.</p>

        <p><b>Tiamat</b> is the goddess of greed and envy. Known as the Chromatic Dragon, Tiamat was born when the dragon god
        Io was split in half, the other god created in this process being her brother Bahamut. She is the mother of the evil
        chromatic dragons. Her followers, both dragon and non-dragon, are encouraged to conquer as much land and hoard as
        much wealth as they can. Few dragons keep shrines to her in their lairs for fear she will demand part of their treasure
        hoard. She holds grudges and never forgives slights. She demands reverence and tribute from her subjects.</p>

        <p><b>Torog</b>, the King that Crawls, is the evil god of the Underdark, patron of jailers and torturers.</p>

        <p><b>Vecna</b> is the evil god of secrets, the undead and necromancy. He is usually depicted as a mummified, desicated
        corpse missing his left hand and left eye. His many titles include the Arch-Lich, the Maimed God and the Whispered
        One. Born a mortal wizard, he ascended to lichdom after assuming a mastery of the dark arts that no mortal had ever
        achieved, before or since. While he does not like the Raven Queen, he prefers her to Orcus as the god of death.</p>

        <p><b>Zehir</b> is the evil god of darkness, poison, and assassins. Snakes are his favored creation, and the yuan-ti revere
        him above all other gods.</p>

